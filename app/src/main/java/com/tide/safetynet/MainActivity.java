public class MainActivity extends Activity {
	
	private Timer _timer = new Timer();
	
	private ImageView imageview1;
	
	private Intent intent = new Intent();
	private TimerTask timer;
	private RequestNetwork reqNetwork;
	private RequestNetwork.RequestListener _reqNetwork_request_listener;
	
	@Override
	protected void onCreate(Bundle _savedInstanceState) {
		super.onCreate(_savedInstanceState);
		setContentView(R.layout.main);
		initialize(_savedInstanceState);
		initializeLogic();
	}
	
	private void initialize(Bundle _savedInstanceState) {
		imageview1 = findViewById(R.id.imageview1);
		reqNetwork = new RequestNetwork(this);
		
		_reqNetwork_request_listener = new RequestNetwork.RequestListener() {
			@Override
			public void onResponse(String _param1, String _param2, HashMap<String, Object> _param3) {
				final String _tag = _param1;
				final String _response = _param2;
				final HashMap<String, Object> _responseHeaders = _param3;
				
			}
			
			@Override
			public void onErrorResponse(String _param1, String _param2) {
				final String _tag = _param1;
				final String _message = _param2;
				
			}
		};
	}
	
	private void initializeLogic() {
		timer = new TimerTask() {
			@Override
			public void run() {
				runOnUiThread(new Runnable() {
					@Override
					public void run() {
						if (SketchwareUtil.isConnected(getApplicationContext())) {
							intent.setClass(getApplicationContext(), WebsiteActivity.class);
							startActivity(intent);
						}
						else {
							intent.setClass(getApplicationContext(), InternetActivity.class);
							startActivity(intent);
						}
					}
				});
			}
		};
		_timer.schedule(timer, (int)(3000));
	}
	
	@Override
	public void onBackPressed() {
		finishAffinity();
	}
}